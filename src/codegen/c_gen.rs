#[cfg(test)]
mod tests {
    use super::*;
    use crate::abi::{Clock, Signal, SignalType};
    use crate::manifest::*;

    fn test_manifest() -> Manifest {
        Manifest {
            project: ProjectConfig {
                name: "test".to_string(),
                version: "0.1.0".to_string(),
            },
            nodes: vec![NodeConfig {
                name: "ctrl".to_string(),
                inputs: vec!["x:real".to_string()],
                outputs: vec!["y:real".to_string()],
                clock: ClockConfig { base_period_ms: 10 },
            }],
            target: TargetConfig {
                platform: "arm-cortex-m".to_string(),
                safety_standard: "DO-178C".to_string(),
            },
            timing: TimingConfig {
                deadline_us: 5000,
                wcet_analysis: true,
            },
            workload: None,
            data: None,
            options: None,
        }
    }

    fn simple_node() -> ParsedNode {
        ParsedNode {
            name: "ctrl".to_string(),
            inputs: vec![Signal::new("x", SignalType::Real)],
            outputs: vec![Signal::new("y", SignalType::Real)],
            clock: Clock::new(10).unwrap(),
            operator_count: 1,
            is_multi_rate: false,
        }
    }

    #[test]
    fn test_header_include_guard() {
        let (header, _) = generate_c_node(&simple_node(), &test_manifest());
        assert!(header.contains("#ifndef CTRL_H"));
        assert!(header.contains("#define CTRL_H"));
        assert!(header.contains("#endif"));
    }

    #[test]
    fn test_header_state_struct() {
        let (header, _) = generate_c_node(&simple_node(), &test_manifest());
        assert!(header.contains("ctrl_state_t"));
        assert!(header.contains("double y_prev"));
        assert!(header.contains("bool _initialised"));
    }

    #[test]
    fn test_header_io_structs() {
        let (header, _) = generate_c_node(&simple_node(), &test_manifest());
        assert!(header.contains("ctrl_input_t"));
        assert!(header.contains("ctrl_output_t"));
        assert!(header.contains("double x;"));
        assert!(header.contains("double y;"));
    }

    #[test]
    fn test_header_function_prototypes() {
        let (header, _) = generate_c_node(&simple_node(), &test_manifest());
        assert!(header.contains("void ctrl_init(ctrl_state_t *state)"));
        assert!(header.contains("void ctrl_step("));
    }

    #[test]
    fn test_source_init_function() {
        let (_, source) = generate_c_node(&simple_node(), &test_manifest());
        assert!(source.contains("void ctrl_init(ctrl_state_t *state)"));
        assert!(source.contains("state->y_prev = 0.0"));
        assert!(source.contains("state->_initialised = true"));
    }

    #[test]
    fn test_source_step_function() {
        let (_, source) = generate_c_node(&simple_node(), &test_manifest());
        assert!(source.contains("void ctrl_step("));
        assert!(source.contains("output->y = input->x + state->y_prev"));
        assert!(source.contains("state->y_prev = output->y"));
    }

    #[test]
    fn test_source_spdx() {
        let (_, source) = generate_c_node(&simple_node(), &test_manifest());
        assert!(source.contains("SPDX-License-Identifier: PMPL-1.0-or-later"));
    }

    #[test]
    fn test_multi_rate_tick_counter() {
        let node = ParsedNode {
            name: "mrate".to_string(),
            inputs: vec![
                Signal::new("fast", SignalType::Real),
                Signal::with_rate("slow", SignalType::Real, 5),
            ],
            outputs: vec![
                Signal::new("out_fast", SignalType::Real),
                Signal::new("out_slow", SignalType::Real),
            ],
            clock: Clock::new(10).unwrap(),
            operator_count: 3,
            is_multi_rate: true,
        };
        let (header, source) = generate_c_node(&node, &test_manifest());
        assert!(header.contains("uint32_t tick_count"));
        assert!(source.contains("tick_count"));
        assert!(source.contains("% 5"));
    }

    #[test]
    fn test_bool_signal_c_type() {
        let node = ParsedNode {
            name: "latch".to_string(),
            inputs: vec![Signal::new("trigger", SignalType::Bool)],
            outputs: vec![Signal::new("held", SignalType::Bool)],
            clock: Clock::new(1).unwrap(),
            operator_count: 1,
            is_multi_rate: false,
        };
        let (header, source) = generate_c_node(&node, &test_manifest());
        assert!(header.contains("_Bool trigger"));
        assert!(header.contains("_Bool held"));
        assert!(source.contains("state->held_prev = false"));
    }
}
