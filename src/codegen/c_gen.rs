// Source generation
// ---------------------------------------------------------------------------

/// Generate the C source file (.c) for a node.
///
/// Contains:
///   - Init function implementation
///   - Step function implementation with deterministic equations
fn generate_source(node: &ParsedNode, manifest: &Manifest) -> String {
    let mut buf = String::with_capacity(4096);

    // File header.
    buf.push_str(&format!(
        "/* Generated by lustreiser — DO NOT EDIT */\n\
         /* SPDX-License-Identifier: PMPL-1.0-or-later */\n\
         /* Node: {} | Target: {} | Standard: {} */\n\n",
        node.name, manifest.target.platform, manifest.target.safety_standard,
    ));

    buf.push_str(&format!("#include \"{}.h\"\n\n", node.name));

    // Init function.
    buf.push_str(&format!(
        "void {}_init({}_state_t *state) {{\n",
        node.name, node.name,
    ));
    for output in &node.outputs {
        buf.push_str(&format!(
            "    state->{}_prev = {};\n",
            output.name,
            c_default_value(&output.signal_type),
        ));
    }
    if node.is_multi_rate {
        buf.push_str("    state->tick_count = 0;\n");
    }
    buf.push_str("    state->_initialised = true;\n");
    buf.push_str("}\n\n");

    // Step function.
    buf.push_str(&format!(
        "void {}_step(\n    {}_state_t *state,\n    const {}_input_t *input,\n    {}_output_t *output\n) {{\n",
        node.name, node.name, node.name, node.name,
    ));

    if node.is_multi_rate {
        // Multi-rate: use tick counter for rate division.
        buf.push_str("    /* Multi-rate clock division */\n");
        buf.push_str("    state->tick_count++;\n\n");

        for (i, output) in node.outputs.iter().enumerate() {
            if i < node.inputs.len() {
                let input_sig = &node.inputs[i];
                if input_sig.rate > 1 {
                    // Sub-rate input: only sample when tick aligns.
                    buf.push_str(&format!(
                        "    /* Rate division: sample '{}' every {} ticks */\n",
                        input_sig.name, input_sig.rate,
                    ));
                    buf.push_str(&format!(
                        "    if (state->tick_count % {} == 0) {{\n",
                        input_sig.rate,
                    ));
                    buf.push_str(&format!(
                        "        output->{} = input->{};\n",
                        output.name, input_sig.name,
                    ));
                    buf.push_str("    } else {\n");
                    buf.push_str(&format!(
                        "        output->{} = state->{}_prev;\n",
                        output.name, output.name,
                    ));
                    buf.push_str("    }\n");
                } else {
                    // Base-rate input: pass through with accumulation.
                    buf.push_str(&format!(
                        "    output->{} = input->{} + state->{}_prev;\n",
                        output.name, input_sig.name, output.name,
                    ));
                }
            } else {
                buf.push_str(&format!(
                    "    output->{} = state->{}_prev;\n",
                    output.name, output.name,
                ));
            }
        }
    } else {
        // Single-rate: straightforward computation.
        buf.push_str("    /* Single-rate: all signals at base clock */\n");
        for (i, output) in node.outputs.iter().enumerate() {
            if i < node.inputs.len() {
                let input_sig = &node.inputs[i];
                buf.push_str(&format!(
                    "    output->{} = input->{} + state->{}_prev;\n",
                    output.name, input_sig.name, output.name,
                ));
            } else if !node.inputs.is_empty() {
                let last_input = &node.inputs[node.inputs.len() - 1];
                buf.push_str(&format!(
                    "    output->{} = input->{} + state->{}_prev;\n",
                    output.name, last_input.name, output.name,
                ));
            }
        }
    }

    // Update state (fby: store current output as previous for next tick).
    buf.push_str("\n    /* Update delay lines (fby state) */\n");
    for output in &node.outputs {
        buf.push_str(&format!(
            "    state->{}_prev = output->{};\n",
            output.name, output.name,
        ));
    }

    buf.push_str("}\n");

    buf
}

/// Return the C default value literal for a signal type.
fn c_default_value(signal_type: &SignalType) -> &'static str {
    match signal_type {
        SignalType::Bool => "false",
        SignalType::Int => "0",
        SignalType::Float => "0.0f",
        SignalType::Real => "0.0",
    }
}

