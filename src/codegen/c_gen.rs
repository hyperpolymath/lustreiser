// Deterministic C code generator for lustreiser.
//
// Generates C99 code from parsed Lustre node definitions. The generated code
// follows the standard Lustre-to-C compilation pattern:
//
//   1. A state struct holding all delay-line values (pre/fby state)
//   2. An init function to zero-initialise the state
//   3. A step function called once per clock tick:
//      - Reads inputs
//      - Computes outputs deterministically from inputs + state
//      - Updates state for the next tick
//
// The generated C is suitable for bare-metal embedded targets (no heap,
// no dynamic dispatch, bounded stack, deterministic execution time).

use super::parser::ParsedNode;
use crate::abi::SignalType;
use crate::manifest::Manifest;

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

/// Generate a C header (.h) and source (.c) file for a single node.
/// Returns (header_content, source_content).
pub fn generate_c_node(node: &ParsedNode, manifest: &Manifest) -> (String, String) {
    let header = generate_header(node, manifest);
    let source = generate_source(node, manifest);
    (header, source)
}

// ---------------------------------------------------------------------------
// Header generation
// ---------------------------------------------------------------------------

/// Generate the C header file (.h) for a node.
///
/// Contains:
///   - Include guard
///   - State struct typedef
///   - Init function prototype
///   - Step function prototype
fn generate_header(node: &ParsedNode, manifest: &Manifest) -> String {
    let guard = format!("{}_H", node.name.to_uppercase());
    let mut buf = String::with_capacity(2048);

    // File header.
    buf.push_str(&format!(
        "/* Generated by lustreiser — DO NOT EDIT */\n\
         /* SPDX-License-Identifier: PMPL-1.0-or-later */\n\
         /* Node: {} | Target: {} | Standard: {} */\n\n",
        node.name, manifest.target.platform, manifest.target.safety_standard,
    ));

    // Include guard.
    buf.push_str(&format!("#ifndef {}\n#define {}\n\n", guard, guard));

    // Required headers.
    buf.push_str("#include <stdint.h>\n");
    buf.push_str("#include <stdbool.h>\n\n");

    // State struct: holds all delay-line values for fby/pre operators.
    buf.push_str(&format!("/**\n * State struct for node '{}'.\n", node.name));
    buf.push_str(" * Holds delay-line values for temporal operators (pre/fby).\n");
    buf.push_str(&format!(
        " * Base clock: {}ms ({:.1}Hz)\n */\n",
        node.clock.base_period_ms,
        node.clock.frequency_hz(),
    ));
    buf.push_str(&format!("typedef struct {{\n"));

    // One state field per output (for fby previous value).
    for output in &node.outputs {
        buf.push_str(&format!(
            "    {} {}_prev;  /**< Previous value of output '{}' */\n",
            output.signal_type.c_type(),
            output.name,
            output.name,
        ));
    }

    // Tick counter for multi-rate scheduling.
    if node.is_multi_rate {
        buf.push_str("    uint32_t tick_count;  /**< Base-clock tick counter for rate division */\n");
    }

    // Initialisation flag.
    buf.push_str("    bool _initialised;  /**< Whether init has been called */\n");

    buf.push_str(&format!("}} {}_state_t;\n\n", node.name));

    // Input struct.
    buf.push_str(&format!("/**\n * Input signals for node '{}'.\n */\n", node.name));
    buf.push_str(&format!("typedef struct {{\n"));
    for input in &node.inputs {
        buf.push_str(&format!(
            "    {} {};  /**< Input signal '{}' (rate: /{}) */\n",
            input.signal_type.c_type(),
            input.name,
            input.name,
            input.rate,
        ));
    }
    buf.push_str(&format!("}} {}_input_t;\n\n", node.name));

    // Output struct.
    buf.push_str(&format!("/**\n * Output signals for node '{}'.\n */\n", node.name));
    buf.push_str(&format!("typedef struct {{\n"));
    for output in &node.outputs {
        buf.push_str(&format!(
            "    {} {};  /**< Output signal '{}' */\n",
            output.signal_type.c_type(),
            output.name,
            output.name,
        ));
    }
    buf.push_str(&format!("}} {}_output_t;\n\n", node.name));

    // Function prototypes.
    buf.push_str(&format!(
        "/**\n * Initialise the state struct for node '{}'.\n",
        node.name
    ));
    buf.push_str(" * MUST be called exactly once before the first step.\n */\n");
    buf.push_str(&format!(
        "void {}_init({}_state_t *state);\n\n",
        node.name, node.name,
    ));

    buf.push_str(&format!(
        "/**\n * Execute one step of node '{}'.\n",
        node.name
    ));
    buf.push_str(&format!(
        " * Called once per base-clock tick (every {}ms).\n",
        node.clock.base_period_ms,
    ));
    buf.push_str(" * Deterministic: same inputs + state => same outputs.\n");
    buf.push_str(&format!(
        " * WCET budget: {}us deadline.\n */\n",
        manifest.timing.deadline_us,
    ));
    buf.push_str(&format!(
        "void {}_step(\n    {}_state_t *state,\n    const {}_input_t *input,\n    {}_output_t *output\n);\n\n",
        node.name, node.name, node.name, node.name,
    ));

    buf.push_str(&format!("#endif /* {} */\n", guard));

    buf
}

